package EY_140823;

public class advancefor {

	public static void main(String[] args) {
		int[] intArr= {1,2,3,4,5};
		for(int data: intArr) {
			System.out.print(data+" ");
		}
		System.out.println();
		String[] strArr= {"1","2","3","4","5"};
		for(String str: strArr) {
			System.out.print(str +" ");
		}

	}

}
